    private static class GLFPainter extends javax.swing.JPanel {

        GeneralisedLogisticFunction f;

        public GLFPainter() {
            super();
            f = new GeneralisedLogisticFunction();

            setPreferredSize(new Dimension(800, 400));
        }

        @Override
        public void paintComponent(Graphics graphics) {
            Graphics2D g = (Graphics2D) graphics;

            XAxis x = new XAxis();
            YAxis y = new YAxis();

            x.labelMin = 0.0;
            x.labelMax = 100.0;
            x.ticks = 25.0;
            x.xmin = 0.0;
            x.xmax = 100.0;
            x.unit = "Â°";
            x.labels = new String[]{"Gerade", "Schnell", "Mittel", "Langsam", "Haarnadel"};

            y.mirror = true;
            y.labelMin = 0.0;
            y.y0 = 50.0;
            y.labelMax = 330.0;
            y.y1 = 330.0;
            y.ticks = 50.0;
            y.unit = "km/h";

            f.paint(g, getSize(), x, y);
        }
    }

